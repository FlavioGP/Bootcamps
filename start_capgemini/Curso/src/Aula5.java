public class Aula5 {

    public static void main(String[] args) {
        int media = 7;

        if (media >= 7) {
            if (media == 10) {
                System.out.println("Aluno aprovado com nota maxima");
            } else {
                System.out.println("Aluno aprovado");
            }
        } else {
            System.out.println("Aluno Reprovado!");
        }
    }
}
